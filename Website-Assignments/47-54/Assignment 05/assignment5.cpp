#include <iostream>
using namespace std;

int main()
{
    //For Loop
    for (int i = 0; i <= 27; i += 3)
    {
        cout << i << endl;
    }
    // While Loop
    cout << "=============\n";
    int i = 0;
    while (i <= 27)
    {
        cout << i << endl;
        i += 3;
    }
    
    return 0;
}